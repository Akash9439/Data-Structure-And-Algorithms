class Solution {
  public:
    // Function to return the count of the number of elements in
    // the intersection of two arrays.
    int NumberofElementsInIntersection(int a[], int b[], int n, int m) {
        // Your code goes here
        unordered_set<int> s;
        int counter=0;
        int i;
        for(i=0;i<n;i++)
        {
            s.insert(a[i]);
        }
        
        for(i=0;i<m;i++)
        {
            if(s.count(b[i])==1)
            {
                counter++;
                s.erase(b[i]);
            }
        }
        return counter;
    }
};

